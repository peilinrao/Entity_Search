         <h1><img src="images/attractions.png" alt="Tropic Utah Attractions " /></h1>
         <p>Tropic Utah is home to many attractions such as Grand Staircase National Monument, Bryce Canyon National Park, and so much more. Our Bryce Valley hotel is located within minutes of these great attractions which makes them easily accessible. There is plenty to do here in Bryce Valley. Come and enjoy hiking, fishing, horseback riding, and so much more!  Americas Best Value Inn is your best choice in Bryce Canyon hotels. Be sure to <a href="http://www.americasbestvalueinn.com/bestv.cfm?idp=1202" target="_blank">check room rates and availability</a> to start your vacation.</p>
         
         <div id="column-c1" />
            <div id="row1">
               <img src="images/attractions/bull-valley-gorge.jpg" alt="" />
               <h2>Bull Valley Gorge</h2>
               <p>This is a great experience for those who enjoy canyoneering and is an extremely fun canyon to hike. The trail follows along the canyon in both directions. With beautiful trees and it's deep narrow canyon, this is an ideal canyon to visit and take lots of pictures. </p>
            </div>
            <div id="row2">
               <img src="images/attractions/cottonwood-canyon.jpg" alt="" />
               <h2>Cottonwood Canyon</h2>
               <p>This hike is fun for all ages. The canyon parallels the road so it makes it easy to turn around and follow the road back. For the more adventurous there is a canyon that takes off from the Cottonwood Canyon and narrows up.</p>
            </div>
            <div id="row3">
               <img src="images/attractions/hole-in-the-rock.jpg" alt="" />
               <h2>Hole in the Rock</h2>
               <p>Hole in the Rock marks where the early pioneers descended to the Colorado River with their wagons. This trail is short but is very steep but is worth the view and walking in the footsteps of the ancient pioneers.</p>
            </div>
            <div id="row4">
               <img src="images/attractions/lake-powell.jpg" alt="" />
               <h2>Lake Powell</h2>
               <p>Enjoy waterskiing, boating, wakeboarding, or just soaking in the sun at the beautiful Lake Powell. This scenic drive will offer plenty of beautiful sites along the way that makes this a great little trip to get out and enjoy the famous Lake Powell.</p>
            </div>
            <div id="row5">
               <img src="images/attractions/powell-point.jpg" alt="" />
               <h2>Powell Point</h2>
               <p>Powell Point is a great vantage point overlooking Grand Staircase Escalante National Monument and Bryce Canyon National Park. This is a must see while staying here at Bryce Valley Inn as you can see what southern Utah is all about.</p>
            </div>
            <div id="row6">
               <img src="images/attractions/zion.jpg" alt="" />
               <h2>Zion National Park</h2>
               <p>With its massive canyon walls, towering cliffs, and beautiful colors, visitors will always remember their visit to Zion National Park. Located near Bryce Valley Inn makes this ideal to get out and enjoy an adventure while being able to come back and relax with us.</p>
            </div>
         </div><!--column-c1-->
                        
         <div id="column-c2">
            <div id="row1">
               <img src="images/attractions/atv-fun.jpg" alt="" /> 
               <h2>ATVing</h2>
               <p>There are plenty of ATV trails located nearby for you to venture out on. Come and enjoy all the beautiful sights and adventurous trails that southern Utah has to offer.  </p>
            </div>
            <div id="row2">
               <img src="images/attractions/burr-trail.jpg" alt="" />
               <h2>Burr Trail</h2>
               <p>This scenic drive extends through Glen Canyon National Recreation area and offers the driver with unbelievable views. All along the trail there are plenty of stops to get out and venture. Travel through beautiful deep red canyons that are truly spectacular.</p>
            </div>
            <div id="row3">
               <img src="images/attractions/grand-canyon.jpg" alt="" />
               <h2>The Grand Canyon</h2>
               <p>The Grand Canyon is a famous site and a must see. Be sure that while you are hear at the Bryce Valley Inn that you make the time to travel to the North Rim of the Grand Canyon for a breath taking view.</p>
            </div>
            <div id="row4">
               <img src="images/attractions/jacob-hamblin-arch.jpg" alt="" />
               <h2>Jacob Hamblin Arch</h2>
               <p>Drive up to the Fortymile Ridge Trailhead and hike into the magnificent Coyote Gulch and just a mere 2 miles in you will arrive at the fascinating Jacob Hamblin Arch. This amazing rock formation makes for some great memories.</p>
            </div>
            <div id="row5">
               <img src="images/attractions/peek-a-boo2.jpg" alt="" />
               <h2>Peek a Boo</h2>
               <p>This hanging canyon has an oval-shaped opening which is 12 feet up on the canyon wall and if you are not paying attention, you will easily walk past. There is small hand and foot holds that will help boost you up into the opening revealing the beautifully sculptured slot canyon.</p>
            </div>
            <div id="row6">
               <img src="images/attractions/round-valley.jpg" alt="" />
               <h2>Round Valley</h2>
               <p>This challenging slot canyon is great for those who want to explore but do not have the gear but do have some canyoneering skills. Just a short little trip from Bryce Valley Inn makes this an ideal visit on your vacation.</p>
            </div>
        </div><!--column-c2-->
            
         <div id="column-c3"> 
            <div id="row1">
               <img src="images/attractions/bryce.jpg" alt="" /> 
               <h2>Bryce Canyon National Park</h2>
               <p>Bryce Canyon National Park is full of many great sites to see. From fascinating structures called hoodoos. There are amazing rock formations, beautiful colors like red, orange, and white for spectacular views. Located near our hotel, this is a perfect attraction in Tropic Utah!</p>
            </div>
            <div id="row2">
               <img src="images/attractions/calf-creek-falls.jpg" alt="" />
               <h2>Calf Creek Falls</h2>
               <p>This is a beautiful 6 mile round trip hike that leads you to the 126 foot lower Calf Creek waterfall. This trail follows the path of the Calf Creek which allows you to take a dip in the heat of the summer. This is a must see waterfall here in Southern Utah.</p>
            </div>
            <div id="row3">
               <img src="images/attractions/grosvenors-arch2.jpg" alt="" />
               <h2>Grosvenors Arch</h2>
               <p>This beautiful double arch is easily accessible and there are restroom facilities and picnic tables there when you drive up. If you are up for and adventure, the climb up to the arch is an experience you will not forget.</p>
            </div>
            <div id="row4">
               <img src="images/attractions/kodachrome2.jpg" alt="" />
               <h2>Kodachrome Basin</h2>
               <p>Kodachrome State Park offers beautiful views with massive sandstone chimneys that are range from gray and white to different shades of red as the day goes on. This year round state park boasts 2240 acres of these beautiful rock formations and amazing sunsets.</p>
            </div>
            <div id="row5">
               <img src="images/attractions/pine-lake.jpg" alt="" />
               <h2>Pine Lake</h2>
               <p>Get out and experience the beauty of Pine Lake and all the fun activities such as boating, fishing, cross country skiing, and snowmobiling. Get out and enjoy this south-central lake for a great experience while staying at the Bryce Valley Inn.</p>
            </div>
            <div id="row6">
               <img src="images/attractions/willis-creek.jpg" alt="" />
               <h2>Willis Creek</h2>
               <p>Willis Creek is a magnificent hike that requires no canonyeering skills and is a hike you will remember as two miles down the canyon the creek runs into Sheep Creek with a rock wall with different pictographs. </p>
